# 데이터 개수가 다른경우의 논리 연산
#1.데이터의 계수가 배수 관계인 경우
a <- c(10,20,30,40,50,60)
b <- c(10,20,30)

a<b
a>b
a>= b

#2. 데이터 개수가 배수관계가 아닌 경우 -> 리사이클링 룰이 적용
a <- c(10,20,30,10,20)
b <- c(10,20,30)

a<b
a>b
a>= b

