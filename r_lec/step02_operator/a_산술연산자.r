#산술연산자
# + - * / ** ^ %% %/% %*%
x <- 2
y <- 3


#단항 +연산
+x

#단항 -연산
-y

#이항 연산 +
x+y

#이향 연산 -
x-y

#나누기 연산 /
x/y

#지수 연산 
x^y

x**y

#나머지 연산 
x%%y

#몫 연산, 정수 나누기
x%/%y

# %*% 행렬곱
x <- c(1,2)
y <- c(3,4)

x%*%y

# /-----------------------------------------------------------

x <- c(T,T,F)
y <- c(T,F,F)

x&y

x|y

any(y)# 뭐라도 하나가 True면 Ture를 리턴

all(y)# 전부 True 일때 True를 리턴

